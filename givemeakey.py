def pbcopy(data):
    # OSX only.. calling pbcopy(1), provide copying to the Clipboard
    # http://www.devx.com/opensource/Article/37233/0/page/3
    p = subprocess.Popen(['pbcopy'], stdin=subprocess.PIPE)
    p.stdin.write(data)
    p.stdin.close()
    retcode = p.wait()
    return retcode

